func TestApply_PriorityOrdering(t *testing.T) {
	scheme := testScheme(t)
	sts := representativeStatefulSet()

	// Two policies write the same annotation; the higher-priority number is
	// applied last and therefore wins.
	low := policy("a-low", 5, slurmv1alpha1.ResourcePatch{
		ResourceRef: nameSel("StatefulSet", "my-cluster-worker"),
		JSONPatch: []slurmv1alpha1.JSONPatchOperation{
			{Op: "add", Path: "/metadata/annotations/winner", Value: jsonValue(t, "low")},
		},
	})
	high := policy("z-high", 10, slurmv1alpha1.ResourcePatch{
		ResourceRef: nameSel("StatefulSet", "my-cluster-worker"),
		JSONPatch: []slurmv1alpha1.JSONPatchOperation{
			{Op: "add", Path: "/metadata/annotations/winner", Value: jsonValue(t, "high")},
		},
	})

	// Pass them out of order to prove sorting happens inside Apply.
	results, err := resourcepatch.Apply(scheme, sts, []slurmv1alpha1.ResourcePatchPolicy{high, low})
	require.NoError(t, err)
	require.Len(t, results, 2)
	assert.Equal(t, "high", sts.Annotations["winner"])
}

