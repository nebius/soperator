func TestApply_FailedPatchDoesNotMutateAndOthersContinue(t *testing.T) {
	scheme := testScheme(t)
	sts := representativeStatefulSet()

	p := policy("mixed", 0,
		slurmv1alpha1.ResourcePatch{
			ResourceRef: nameSel("StatefulSet", "my-cluster-worker"),
			// "test" op against a non-matching value fails.
			JSONPatch: []slurmv1alpha1.JSONPatchOperation{
				{Op: "test", Path: "/metadata/name", Value: jsonValue(t, "wrong-name")},
			},
		},
		slurmv1alpha1.ResourcePatch{
			ResourceRef: nameSel("StatefulSet", "my-cluster-worker"),
			JSONPatch: []slurmv1alpha1.JSONPatchOperation{
				{Op: "add", Path: "/metadata/annotations/good", Value: jsonValue(t, "yes")},
			},
		},
	)

	results, err := resourcepatch.Apply(scheme, sts, []slurmv1alpha1.ResourcePatchPolicy{p})
	require.NoError(t, err)
	require.Len(t, results, 2)
	assert.False(t, results[0].Applied)
	assert.NotEmpty(t, results[0].Message)
	assert.True(t, results[1].Applied, "message: %s", results[1].Message)
	assert.Equal(t, "yes", sts.Annotations["good"])
	assert.Equal(t, "my-cluster-worker", sts.Name, "failed patch must not have mutated the object")
}

