func TestApply_JSONMergePatch(t *testing.T) {
	scheme := testScheme(t)
	cm := &corev1.ConfigMap{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "my-cluster-slurm-config",
			Namespace: "default",
		},
		Data: map[string]string{"slurm.conf": "base"},
	}

	p := slurmv1alpha1.ResourcePatchPolicy{
		ObjectMeta: metav1.ObjectMeta{Name: "merge", Namespace: "default"},
		Spec: slurmv1alpha1.ResourcePatchPolicySpec{
			TargetRef: slurmv1alpha1.PolicyTargetReference{
				Group: "slurm.nebius.ai", Kind: "SlurmCluster", Name: "my-cluster",
			},
			Type: slurmv1alpha1.JSONMergePatchType,
			Patches: []slurmv1alpha1.ResourcePatch{
				{
					ResourceRef: nameSel("ConfigMap", "my-cluster-slurm-config"),
					JSONMergePatch: jsonValue(t, map[string]any{
						"metadata": map[string]any{
							"annotations": map[string]string{"custom-key": "custom-value"},
						},
						"data": map[string]string{"extra.conf": "SomeExtraConfig=value"},
					}),
				},
			},
		},
	}

	results, err := resourcepatch.Apply(scheme, cm, []slurmv1alpha1.ResourcePatchPolicy{p})
	require.NoError(t, err)
	require.Len(t, results, 1)
	assert.True(t, results[0].Applied, "message: %s", results[0].Message)
	assert.Equal(t, "custom-value", cm.Annotations["custom-key"])
	assert.Equal(t, "SomeExtraConfig=value", cm.Data["extra.conf"])
	assert.Equal(t, "base", cm.Data["slurm.conf"], "merge patch must preserve existing keys")
}

func TestApply_ExactNameSelector(t *testing.T) {
	scheme := testScheme(t)

	tests := []struct {
		name     string
		selector string
		objName  string
		want     bool
	}{
		{"exact match", "my-cluster-worker", "my-cluster-worker", true},
		{"exact mismatch", "my-cluster-worker", "my-cluster-login", false},
		// Glob patterns are not interpreted: they only match a literal name.
		{"glob is treated literally", "my-cluster-worker-*", "my-cluster-worker-0", false},
		{"glob literal exact", "my-cluster-worker-*", "my-cluster-worker-*", true},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			sts := representativeStatefulSet()
			sts.Name = tt.objName
			p := policy("p", 0, slurmv1alpha1.ResourcePatch{
				ResourceRef: slurmv1alpha1.ResourceSelector{Kind: "StatefulSet", Name: ptr.To(tt.selector)},
				JSONPatch: []slurmv1alpha1.JSONPatchOperation{
					{Op: "add", Path: "/metadata/annotations/patched", Value: jsonValue(t, "yes")},
				},
			})
			results, err := resourcepatch.Apply(scheme, sts, []slurmv1alpha1.ResourcePatchPolicy{p})
			require.NoError(t, err)
			if tt.want {
				require.Len(t, results, 1)
				assert.True(t, results[0].Applied)
				assert.Equal(t, "yes", sts.Annotations["patched"])
			} else {
				assert.Empty(t, results, "selector should not have matched")
				_, ok := sts.Annotations["patched"]
				assert.False(t, ok)
			}
		})
	}
}

