func TestApply_ProtectedFields(t *testing.T) {
	scheme := testScheme(t)

	tests := []struct {
		name string
		op   slurmv1alpha1.JSONPatchOperation
	}{
		{
			name: "name",
			op:   slurmv1alpha1.JSONPatchOperation{Op: "replace", Path: "/metadata/name", Value: jsonValue(t, "renamed")},
		},
		{
			name: "namespace",
			op:   slurmv1alpha1.JSONPatchOperation{Op: "replace", Path: "/metadata/namespace", Value: jsonValue(t, "other")},
		},
		{
			name: "ownerReferences",
			op: slurmv1alpha1.JSONPatchOperation{Op: "add", Path: "/metadata/ownerReferences", Value: jsonValue(t, []metav1.OwnerReference{
				{APIVersion: "v1", Kind: "Pod", Name: "x", UID: "123"},
			})},
		},
		{
			name: "selector",
			op: slurmv1alpha1.JSONPatchOperation{Op: "replace", Path: "/spec/selector/matchLabels", Value: jsonValue(t, map[string]string{
				"app.kubernetes.io/component": "hijacked",
			})},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			sts := representativeStatefulSet()
			p := policy("protected", 0, slurmv1alpha1.ResourcePatch{
				ResourceRef: nameSel("StatefulSet", "my-cluster-worker"),
				JSONPatch:   []slurmv1alpha1.JSONPatchOperation{tt.op},
			})
			results, err := resourcepatch.Apply(scheme, sts, []slurmv1alpha1.ResourcePatchPolicy{p})
			require.NoError(t, err)
			require.Len(t, results, 1)
			assert.False(t, results[0].Applied, "protected field %s must be rejected", tt.name)
			assert.NotEmpty(t, results[0].Message)
			// The object must be unchanged.
			assert.Equal(t, "my-cluster-worker", sts.Name)
			assert.Equal(t, "default", sts.Namespace)
			assert.Empty(t, sts.OwnerReferences)
			assert.Equal(t, "worker", sts.Spec.Selector.MatchLabels["app.kubernetes.io/component"])
		})
	}
}

