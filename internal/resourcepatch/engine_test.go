func testScheme(t *testing.T) *runtime.Scheme {
	t.Helper()
	scheme := runtime.NewScheme()
	require.NoError(t, clientgoscheme.AddToScheme(scheme))
	require.NoError(t, slurmv1alpha1.AddToScheme(scheme))
	return scheme
}

// jsonValue marshals v into an *apiextensionsv1.JSON for use as a JSON Patch
// operation value.
func jsonValue(t *testing.T, v any) *apiextensionsv1.JSON {
	t.Helper()
	raw, err := json.Marshal(v)
	require.NoError(t, err)
	return &apiextensionsv1.JSON{Raw: raw}
}

func mustQuantity(s string) resource.Quantity {
	return resource.MustParse(s)
}

// policy builds a JSONPatch policy targeting a SlurmCluster.
func policy(name string, priority int32, patches ...slurmv1alpha1.ResourcePatch) slurmv1alpha1.ResourcePatchPolicy {
	return slurmv1alpha1.ResourcePatchPolicy{
		ObjectMeta: metav1.ObjectMeta{Name: name, Namespace: "default"},
		Spec: slurmv1alpha1.ResourcePatchPolicySpec{
			TargetRef: slurmv1alpha1.PolicyTargetReference{
				Group: "slurm.nebius.ai",
				Kind:  "SlurmCluster",
				Name:  "my-cluster",
			},
			Priority: ptr.To(priority),
			Type:     slurmv1alpha1.JSONPatchType,
			Patches:  patches,
		},
	}
}

func nameSel(kind, name string) slurmv1alpha1.ResourceSelector {
	return slurmv1alpha1.ResourceSelector{Kind: kind, Name: ptr.To(name)}
}

// representativeStatefulSet mirrors the structure of a controller/worker
// StatefulSet generated by the operator (one main container, labelled pod
// template, immutable selector).
func representativeStatefulSet() *appsv1.StatefulSet {
	return &appsv1.StatefulSet{
		ObjectMeta: metav1.ObjectMeta{
			Name:        "my-cluster-worker",
			Namespace:   "default",
			Labels:      map[string]string{"app.kubernetes.io/component": "worker"},
			Annotations: map[string]string{"existing": "true"},
		},
		Spec: appsv1.StatefulSetSpec{
			Replicas: ptr.To(int32(3)),
			Selector: &metav1.LabelSelector{
				MatchLabels: map[string]string{"app.kubernetes.io/component": "worker"},
			},
			Template: corev1.PodTemplateSpec{
				ObjectMeta: metav1.ObjectMeta{
					Labels:      map[string]string{"app.kubernetes.io/component": "worker"},
					Annotations: map[string]string{"kubectl.kubernetes.io/default-container": "slurmd"},
				},
				Spec: corev1.PodSpec{
					Containers: []corev1.Container{
						{
							Name:  "slurmd",
							Image: "slurmd:latest",
							Resources: corev1.ResourceRequirements{
								Limits: corev1.ResourceList{corev1.ResourceCPU: resource.MustParse("500m")},
							},
						},
					},
				},
			},
		},
	}
}

